    <div class="nota-body">

      <p>Il Bilancio di Sostenibilità rappresenta per noi uno strumento prezioso di misurazione e comunicazione: ci permette infatti di descrivere in modo trasparente ai nostri stakeholder gli impatti sociali, economici e ambientali legati alle nostre attività. Ci permette inoltre di individuare opportunità di crescita e sviluppo in ambito ESG, ponendo le basi per definire obiettivi concreti a medio-lungo termine e strutturare un Piano Strategico di Sostenibilità.</p>

      <p>Per questo abbiamo deciso di rendicontare in maniera volontaria i nostri impegni, obiettivi e risultati in un Bilancio di Sostenibilità a cadenza annuale, anche se non siamo soggetti a un obbligo normativo secondo la legislazione attualmente in vigore (Direttiva 2014/95/UE, recepita in Italia attraverso il Decreto Legislativo 30 dicembre 2016, n. 254).</p>

      <p>La redazione del Bilancio è stata curata da un gruppo di lavoro interno con il supporto di ALTIS Advisory Società Benefit, spin-off dell'Università Cattolica del Sacro Cuore che offre servizi di supporto agli attori economici per migliorarne la sostenibilità.</p>

      <p>I dati contenuti nel documento sono stati raccolti grazie alla collaborazione dei responsabili di funzione, utilizzando tool specifici, dove presenti, o sistemi di archiviazione interna. Trattandosi di una rendicontazione volontaria, il documento non è stato sottoposto ad audit esterno.</p>

      <p>La rendicontazione del presente Bilancio fa riferimento all'esercizio 2025, che va dal 1° maggio 2024 al 30 aprile 2025.</p>

      <p>Il documento è ispirato agli European Sustainability Reporting Standards (ESRS) elaborati dall'European Financial Reporting Advisory Group (EFRAG) e adottati ufficialmente dalla Commissione Europea come nuovi standard unici per la rendicontazione di sostenibilità delle aziende soggette alla CSRD.</p>

      <p>Per qualsiasi informazione relativa al presente Bilancio di Sostenibilità è possibile scrivere alla mail: <a href="mailto:info@vargroup.com">info@vargroup.com</a> oppure visitare il sito <a href="https://varplace.vargroup.com/Su-di-noi/Sostenibilita" target="_blank" rel="noopener noreferrer">varplace.vargroup.com</a>.</p>

